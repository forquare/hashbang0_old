<h1>Brackets</h1>

<p>
	In programming, many types of brackets are used for different things.  In this section I will explain them, though you may not know what the heck I am going on about, you will once you read later sections.
</p>

<p>
	Our first type of bracket is curly: { }<br />
	These brackets enclose functions, statements and other such things.  Often you will have a statement, then open one of these brackets, have some commands, then close the bracket.
</p>

<p>
	Another bracket is the round bracket: ( )<br />
	These typically hold variables or other values, you will often see them at the top of functions, you will also use them when calling functions.  They can be empty or jammed packed of values depending on what that line of code does...
</p>

<p>
	The square brackets are also used in many languages: [ ]<br />
	These often denote a collection of values (see arrays).
</p>

<p>
	Angle brackets: &lt; &gt;<br />
	These are only used for mathematic stuffs of greater/less than...
</p>