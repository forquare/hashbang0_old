<h1>Data Types</h1>

<p>
	In programming, you use a variety of different types of data.  Various languages allow you to use and manipulate your data in different ways.
</p>

<p>
	Common data types are:
</p>

<ul>
	<li>Character</li>
	<li>Integer</li>
	<li>Float</li>
	<li>Double</li>
	<li>String</li>
</ul>

<p>
	The first two are often shortened to "char" and "int", these are just the keywords to use them.
</p>

<p>
	It should be noted that the "String" type is an array (or string) of characters (more on arrays later).  In some languages, the String type is defined for you!  You just say "I want a string please".  In other language you have to create if from scratch by using arrays of characters.
</p>

<p>
	It might also be worth to note here that there are specific ways to write variables...<br />
	Integers are just written as they appear, so: 42.<br />
	Strings are most commonly written with double quotes: "Hello, I'm a string!"<br />
	Characters are written with single quotes: 'c'
</p>