<h1>Compilation</h1>

<p>
	Once you have written your program, you have to compile it.  Compiling translates all the code you have written into a lower-level computing language, a language that your computer understands.
</p>

<p>
	Compilation is often done via a command line to begin with, later, you may use an Integrated Development Environment (IDE) which does all sorts of things for you.
</p>

<p>
	Different languages compile differently, or rather, different compilers compile differently.  
</p>

<p>
	If this all seems a tad hazy, don't worry, it can feel a bit much to begin with.  In essence, there is a step in the programming stage where you need to convert your program to be able to run it.
</p>