<h1>Functions</h1>

<p>
	Functions are a way of grouping together a sequence of commands.  The function can use variables that are passed to it, or return a value.
</p>

<p>
	What I just said probably made little sense without me explaining that you can call functions, when you create a function you give it a name, when you want to use that sequence of instructions, you just call the function by it's name.  Note that nearly all commands are placed in some kind of function.
</p>

<p>
	In many languages, the first function called by the OS when the program is executed is a function called "main".
</p>

<p>
	Also to quickly note, I will often refer to functions as "methods", this is just another name for functions.
</p>