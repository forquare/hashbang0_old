<h1>Variables</h1>

<p>
	When programming, sometimes we want our program to remember things.  Take the previous example of typing in your name, how does the computer remember what you typed in?  What happens if the program wants to use your name again?
</p>

<p>
	We have a solution: Variables!
</p>

<p>
	Variables are like boxes that you can store values in.  What you can put in the box depends on what language you are using, but they are generally split up into a number of types (see next page).  Whatever you put in the box, you can change or manipulate, hence the name 'variable'.
</p>

<p>
	Just to confuse things, there are variables called constants, which are just like variables, except you can't change the value (i.e. the value stays constant).
</p>

<p>
	Variables usually need to be declared before you use them.  Basically, this tells the program that they are there, what data type they are etc. just helps the program to know what your on about.
</p>