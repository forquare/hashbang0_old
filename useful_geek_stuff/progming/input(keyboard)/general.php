<h1>Reading from the keyboard</h1>

<p>
	It's all very well jabbering away to your user, but if they can't interact with your program, they will probably force it closed and lock it away for good!
</p>

<p>
	Never fear!  We have a means to communicate across the void of cyber-space to the outside world...The Keyboard!
</p>

<p>
	We can enable users to interact with out programs by strategically letting them make decisions, influencing how our program runs.  Typically, for a first time program, we might have this scenario:
</p>

<p>
	Computer: What is your name?<br />
	You: Dave<br />
	Computer: Hello Dave!<br />
</p>

<p>
	Later we can create really complex scenarios where what your users types into the keyboard can decide which decision to make.
</p>