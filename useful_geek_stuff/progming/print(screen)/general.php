<h1>Printing to the screen</h1>

<p>
	Your first few programs will be based in a command line, it is important to be able to print things to the screen.
</p>

<p>
	Primarily, there are two types of printing to the screen:<br />
	1. Print, no new line<br />
	2. Print, go to next line<br />
</p>

<p>
	There is various formatting you can usually put in place to force new lines and other formatting stuffs, like tabs and the like.
</p>

<p>
	In most languages, the keyword for printing to the screen is "print".  This is, apparently, due to the historical roots of programming, back when the output was literally printed from a printer.  I read it in a book somewhere, so I guess it must be true...
</p>