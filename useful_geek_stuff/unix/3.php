<h2>SSH</h2>

<p>
	SSH is a program for logging on to a remote system.  Once logged on, you can execute commands as if you were sitting at a keyboard connected to that system!
</p>

<p>
	All you need do is: <span class="code">ssh IP/Location-name -l userID</span><br />
	This will then try to connect to the machine at the given IP address or at the location specified and prompt for a password.<br />
