<h2>Home</h2>

<p>
	UNIX is a brilliant system.  Designed in the 1960's at Bell Labs by a group of AT&amp;T employees.  Today, "UNIX" describes a set of standards (core functionality) for operating systems. Check out this link for more info (<a href="http://www.levenez.com/unix/">clicky</a>).
</p>

<p>
	You can use UNIX in many different flavours.  People most commonly use a form of Linux, these pages don't go into what Linux distributions are the best, as that is down to personal choice, however, I don't use Linux often, I use the following two systems much more regularly: 
	<a href="http://www.apple.com/macosx/">Mac OS Snow Leopard</a> and <a href="http://hub.opensolaris.org">OpenSolaris</a>.
</p>

<p>
	OS X is based around <a href="http://en.wikipedia.org/wiki/Berkeley_Software_Distribution">BSD</a>.  BSD started out by sharing some of the original UNIX codebase, as it has been modified it has lost a percentage of that codebase.  Today BSD is predominantly used in the form of Apple's proprietary Mac OS, or free distributions such as <a href="http://www.freebsd.org/">FreeBSD</a>, <a href="http://www.openbsd.org/">OpenBSD</a> and <a href="http://www.netbsd.org/">NetBSD</a>.
</p>	

<p>
	SunOS was developed by <a href="http://sun.com">Sun Microsystems</a> in the early 1980's, it was based around BSD up to SunOS 4.1.4, as of SunOS 5 Sun changed the name to Solaris and it shifted from being based on BSD to being based on <a href="http://en.wikipedia.org/wiki/System_V">System V</a>.  As of System V Release 4 (SVR4, System Five Release Four), the system includes technology from SVR3, 4.3BSD, Xenix and SunOS.  In January 2005 Sun released the source for OpenSolaris and have progressed into creating the OpenSolaris distribution which you can download from the link above.  In 2009 Sun was taken over by database giant, Oracle.  From what I can make out, much of the Sun site now sits on the Oracle site.
</p>

