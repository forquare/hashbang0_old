<h2>Computing</h2>

<p>
	My interests in computing mainly revolve around system administration and the UNIX and UNIX-like operating systems.  <br />
	I regularly use Mac OS X (currently Mac OS 10.6 Snow Leopard) on a 2006 MacBook and a 2009 Mac Pro for desktop functionality and OpenSolaris for server functionality (centralised storage and NFS file sharing between several Mac computers).  I have, in the past, used Sun Rays as a form of desktop, I think this is an excellent medium for offices offering best value for money.
</p>

<h2>Industrial Year</h2>

<p>
	While at Sun I went on a number of courses  Two courses about some of the older, mid-range Sun Fire and high-range Sun Fire servers.  Two courses about Administering Solaris 10 which included everything from administering disks and file systems to users and networks.  I was also very fortunate to take the Solaris 10 Internals course which was mainly about how the Solaris kernel is structures and how Solaris operates, it also looked into how UFS works internally and had a limited amount of material on ZFS.
</p>

<h2>University</h2>

<p>
	After three years of study plus an industrial year at Sun Microsystems I graduated from the <a href="http://www.aber.ac.uk">Aberystwyth University</a> with a 2:1 in <a href="http://www.aber.ac.uk/en/cs">Computer Science</a>.<br />
	University has given me a good basis for my current knowledge.  It has taught me how to program in Java, but more importantly how to use the language in an Object Orientated fashion.  I was first introduced to UNIX in my first year and since then have been hooked on it, unfortunately there was very little else in my course associated with UNIX or any operating system.<br />
	I have a basic understanding of hardware at a low level, and how hardware might be used in mobile, embedded, wearable, ubiquitous and real-time systems thanks to a number of modules over the years.
</p>

<h2>Open and Closed Source</h2>

<p>
	I am a great believer in open source technologies.  To date I haven't taken part in any open source project other than asking and answering questions on various OpenSolaris forums.  Although I love open source and will support it where I can, if a closed source application fulfils a need and seems good value to me, I will often buy it.<br />
	In the near future I want to study the Objective-C language and help the <a href="http://www.caminobrowser.org">Camino Web Browser</a> community by fixing bugs and adding extra functionality.
</p>

<h2>Tools</h2>

<p>
	For programming, I mainly use the Eclipse IDE, though I have some knowledge of Netbeans.  For small projects, or single file projects, I will use either Vim or <a href="http://macromates.com/">TextMate</a> and a command line interpreter/compiler.
</p>

<p>
	For basic word processing and any spreadsheet work I use OpenOffice, for everything I need to do it has been a suitable suite of tools.  I do have a background in Microsoft Office, but at this time don't feel that the gains in functionality are worth the extra money.  For larger documents/reports I use LaTeX using the TeXShop editor.
</p>

<p>
	My presentations are often done using Apple's Keynote application as it provides a simple and clean interface allowing for simple and clean slides, with OpenOffice's presenter and Microsoft Office's PowerPoint, I feel there is always a tendency to clutter slides and make them more complex than needed.
</p>

<p>
	Without the Bash shell, much of my work would take a long time.  Thanks to the Bash shell and it's ability to script, a lot of my time is freed up thanks to automation.
</p>

